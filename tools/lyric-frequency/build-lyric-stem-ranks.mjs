import fs from "node:fs";
import path from "node:path";
import readline from "node:readline";

const inputFile = "data/lyric-frequency/raw/full_word_list.txt";

const outRanks = "src/lib/lyric-stem-ranks.ts";
const outStats = "data/lyric-frequency/lyric-stem-stats.json";
const outTopCsv = "data/lyric-frequency/top-lyric-stems.csv";

const MAX_RANKS_TO_EXPORT = 50_000;

function isCleanAsciiStem(value) {
  return /^[a-z]+$/.test(value);
}

const counts = new Map();

async function processFile(filePath) {
  if (!fs.existsSync(filePath)) {
    throw new Error(`Missing input file: ${filePath}`);
  }

  const stream = fs.createReadStream(filePath, "utf8");
  const rl = readline.createInterface({ input: stream, crlfDelay: Infinity });

  let rawRows = 0;
  let keptRows = 0;
  let skippedRows = 0;

  for await (const line of rl) {
    const trimmed = line.trim();

    if (!trimmed || trimmed.startsWith("#")) {
      continue;
    }

    rawRows += 1;

    const [stem, rawCount] = trimmed.split("<SEP>");
    const count = Number(rawCount);

    if (!stem || !Number.isFinite(count) || count <= 0) {
      skippedRows += 1;
      continue;
    }

    // The source contains some non-English / encoded / punctuated fragments.
    // Keep only clean lowercase ASCII stems so scoring is stable.
    if (!isCleanAsciiStem(stem)) {
      skippedRows += 1;
      continue;
    }

    counts.set(stem, (counts.get(stem) ?? 0) + count);
    keptRows += 1;
  }

  console.log(`Raw data rows: ${rawRows.toLocaleString()}`);
  console.log(`Kept clean stems: ${keptRows.toLocaleString()}`);
  console.log(`Skipped rows: ${skippedRows.toLocaleString()}`);
}

await processFile(inputFile);

const ranked = [...counts.entries()]
  .sort((a, b) => {
    const countDiff = b[1] - a[1];
    if (countDiff !== 0) return countDiff;
    return a[0].localeCompare(b[0]);
  })
  .map(([stem, tokenCount], index) => ({
    stem,
    rank: index + 1,
    tokenCount,
  }));

const exportedRanks = ranked.slice(0, MAX_RANKS_TO_EXPORT);

const rankEntries = exportedRanks
  .map(({ stem, rank }) => `  ${JSON.stringify(stem)}: ${rank},`)
  .join("\n");

const tsOutput = `// Generated by tools/lyric-frequency/build-lyric-stem-ranks.mjs
// Derived from the musiXmatch / Million Song Dataset full word list.
// This file contains lyric stem ranks only, not lyrics.
// Do not edit by hand.

export const LYRIC_STEM_RANKS: Record<string, number> = {
${rankEntries}
} as const;
`;

fs.mkdirSync(path.dirname(outRanks), { recursive: true });
fs.writeFileSync(outRanks, tsOutput, "utf8");

fs.mkdirSync(path.dirname(outStats), { recursive: true });
fs.writeFileSync(outStats, JSON.stringify(ranked, null, 2), "utf8");

const csvRows = [
  "rank,stem,tokenCount",
  ...ranked.slice(0, 1000).map(({ rank, stem, tokenCount }) => {
    return `${rank},${JSON.stringify(stem)},${tokenCount}`;
  }),
];

fs.writeFileSync(outTopCsv, csvRows.join("\n"), "utf8");

console.log("");
console.log(`Ranked ${ranked.length.toLocaleString()} clean lyric stems.`);
console.log(`Exported top ${exportedRanks.length.toLocaleString()} ranks to ${outRanks}.`);
console.log(`Wrote ${outStats}.`);
console.log(`Wrote ${outTopCsv}.`);

