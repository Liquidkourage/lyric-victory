for (const stem of auditStems) {
  const entry = byStem.get(stem);
  if (!entry) {
    console.log(`${stem}: missing`);
  } else {
    console.log(
      `${stem}: rank ${entry.rank.toLocaleString()}, tokenCount ${entry.tokenCount.toLocaleString()}`,
    );
  }
}